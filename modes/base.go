package modes

import (
	tea "github.com/charmbracelet/bubbletea"
)

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			ModeStrategy defines the interface for different operational modes.
//
//		@Description	Implements the Strategy pattern for switching between Switch, Create, and Rename modes
//
// ///////////////////////////////////////////////////////////////////////////////////////////
type ModeStrategy interface {
	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			Update processes a message and returns the next mode state and optional command.
	//
	//	@Param			msg		tea.Msg			Input message (keyboard, window resize, etc.)
	//
	//	@Return			ModeStrategy	Next mode state (may be different mode)
	//	@Return			tea.Cmd			Optional command to execute
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	Update(msg tea.Msg) (ModeStrategy, tea.Cmd)

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			View returns the string representation of the mode's UI.
	//
	//	@Return			string	Rendered view content
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	View() string

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			ModeName returns the display name of the current mode.
	//
	//	@Return			string	Mode name (e.g., "SWITCH", "CREATE", "RENAME")
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	ModeName() string

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			Reset clears the mode's internal state.
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	Reset()

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			GetCurrentSession returns the currently selected session name.
	//
	//	@Return			string	Selected session name or empty string
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	GetCurrentSession() string

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			GetIcon returns the icon to display for this mode.
	//
	//	@Return			string	Nerd font icon
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	GetIcon() string

	/////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  @Brief			GetFooterText returns the help text to display in the footer.
	//
	//	@Return			string	Footer help text with available keybindings
	//
	/////////////////////////////////////////////////////////////////////////////////////////////
	GetFooterText() string
}
