package modes

import (
	"strings"

	"github.com/charmbracelet/bubbles/textinput"
	tea "github.com/charmbracelet/bubbletea"

	"github.com/jkeresman01/tsm/tmux"
	"github.com/jkeresman01/tsm/utils"
)

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			SwitchMode handles switching between existing tmux sessions.
//
//		@Description	Provides fuzzy search and keyboard navigation for session selection
//
// ///////////////////////////////////////////////////////////////////////////////////////////
type SwitchMode struct {
	sessions []string        // All available tmux sessions
	filtered []string        // Filtered sessions based on search query
	cursor   int             // Currently selected index
	input    textinput.Model // Search input field
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			NewSwitchMode creates a new SwitchMode instance.
//
//		@Param			sessions	[]string	List of available tmux sessions
//
//		@Return			*SwitchMode	Initialized SwitchMode
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func NewSwitchMode(sessions []string) *SwitchMode {
	return &SwitchMode{
		sessions: sessions,
		filtered: sessions,
		input:    newSwitchInput(),
	}
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			Update processes input messages and updates the mode state.
//
//		@Param			msg		tea.Msg			Input message
//
//		@Return			ModeStrategy	Updated mode state
//		@Return			tea.Cmd			Optional command
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) Update(msg tea.Msg) (ModeStrategy, tea.Cmd) {
	if key, ok := msg.(tea.KeyMsg); ok {
		if next, cmd, done := m.handleKey(key); done {
			return next, cmd
		}
	}
	cmd := m.updateInput(msg)
	m.applyFilter()
	m.clampCursor()
	return m, cmd
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			View renders the mode's UI.
//
//		@Return			string	Rendered view with session list
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) View() string {
	q := m.query()
	var b strings.Builder
	for i, s := range m.filtered {
		b.WriteString(m.rowPrefix(i))
		b.WriteString(utils.HighlightMatches(s, q))
		b.WriteByte('\n')
	}
	return b.String()
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			ModeName returns the display name of this mode.
//
//		@Return			string	"SWITCH MODE"
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) ModeName() string { return "SWITCH MODE" }

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	@Brief			Reset clears the search input.
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) Reset() { m.input.Reset() }

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			GetCurrentSession returns the currently selected session.
//
//		@Return			string	Selected session name or empty string
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) GetCurrentSession() string {
	if m.hasSelection() {
		return m.filtered[m.cursor]
	}
	return ""
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			newSwitchInput creates a configured text input for search.
//
//		@Return			textinput.Model	Configured input field
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func newSwitchInput() textinput.Model {
	ti := textinput.New()
	ti.Placeholder = "Search sessions..."
	ti.Focus()
	ti.Prompt = ""
	ti.CharLimit = 64
	ti.Width = 20
	return ti
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			handleKey processes keyboard input.
//
//		@Param			k		tea.KeyMsg		Keyboard message
//
//		@Return			ModeStrategy	Next mode (if changed)
//		@Return			tea.Cmd			Command to execute
//		@Return			bool			Whether key was handled
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) handleKey(k tea.KeyMsg) (ModeStrategy, tea.Cmd, bool) {
	switch k.String() {
	case "up", "k":
		m.moveCursor(-1)
	case "down", "j":
		m.moveCursor(1)
	case "enter":
		if m.hasSelection() {
			tmux.AttachSession(m.filtered[m.cursor])
			return m, tea.Quit, true
		}
	case "d", "delete":
		if m.hasSelection() {
			// Does nothing in this exact moment in the universe
		}
	}
	return nil, nil, false
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			updateInput updates the search input field.
//
//		@Param			msg		tea.Msg		Input message
//
//		@Return			tea.Cmd	Command from input update
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) updateInput(msg tea.Msg) tea.Cmd {
	var cmd tea.Cmd
	m.input, cmd = m.input.Update(msg)
	return cmd
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			GetIcon returns the mode's icon.
//
//		@Return			string	Nerd font icon
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) GetIcon() string {
	return "󰆧"
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			GetFooterText returns the help text for the footer.
//
//		@Return			string	Keybinding help text
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) GetFooterText() string {
	return "↑↓ navigate • ↵ switch • ⇥ cycle • ^N new • ^R rename • ? help • q quit"
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	@Brief			applyFilter filters sessions based on search query.
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) applyFilter() {
	m.filtered = utils.FuzzyFilter(m.sessions, m.query())
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	@Brief			clampCursor ensures cursor stays within valid range.
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) clampCursor() {
	n := len(m.filtered)
	if n == 0 {
		m.cursor = 0
		return
	}
	if m.cursor < 0 {
		m.cursor = 0
	} else if m.cursor >= n {
		m.cursor = n - 1
	}
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			moveCursor moves the cursor by delta positions.
//
//		@Param			delta	int	Number of positions to move (negative for up)
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) moveCursor(delta int) {
	if len(m.filtered) == 0 {
		m.cursor = 0
		return
	}
	m.cursor += delta
	m.clampCursor()
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			hasSelection returns whether a valid session is selected.
//
//		@Return			bool	True if a session is selected
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) hasSelection() bool {
	return len(m.filtered) > 0 && m.cursor >= 0 && m.cursor < len(m.filtered)
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			query returns the current search query.
//
//		@Return			string	Search query text
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) query() string {
	return m.input.Value()
}

// ///////////////////////////////////////////////////////////////////////////////////////////
//
//	 @Brief			rowPrefix returns the prefix for a list row.
//
//		@Param			i		int		Row index
//
//		@Return			string	Prefix ("> " for selected, "  " for others)
//
// ///////////////////////////////////////////////////////////////////////////////////////////
func (m *SwitchMode) rowPrefix(i int) string {
	if i == m.cursor {
		return "> "
	}
	return "  "
}
